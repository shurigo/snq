                    	<p>Когда-то они были пределом мечтания советского человека, а теперь сложно себе представить человека, у которого бы их не было. Да, это джинсы. Помню, что в студенческие годы я совершенно отказывалась их носить, так как считала джинсы абсолютно не женственными. И даже сейчас коллеги в редакции, увидев меня в джинсах очень удивляются, ведь за мной прочно утвердилась репутация девушки в платье. Хотя каких только джинсов в моем гардеробе нет:  расклешенные, прямые, скинни, с дырочками и без, темно-синие и  почти бесцветные, парочка мужских джинсов и джинсовых комбинезонов. А еще юбки, пиджаки и куртки. Кажется, даже сумка где-то была.</p>
