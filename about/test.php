        <p><img src="/images/blog/publ10/publ18_1.jpg" width="130" height="195" align="left" style="margin:3px 10px 10px 0;">Вот и наступил сентябрь. Первое, на что начинаешь обращать внимание в это время – большое количество школьников на улицах – в формах и с рюкзаками. Пытаетесь понять, почему я вспомнила именно про них? Все просто! Один из главных трендов осенне-зимнего сезона – это клетка. А школьная форма – это, прежде всего, клетчатая плиссированная юбка. Именно такой вариант будет одним из самых популярных в ближайшие несколько месяцев. Однако, модная тенденция наступившего сезона – это не только юбки соответствующей расцветки, но и платья! Фасоны приветствуются самые разные: от классической плиссировки до прямого кроя. И по длине нет особых ограничений -  выбирайте, что душе угодно! Звездные модницы уже подхватили модный тренд. Я советую, и вам не отставать!
<p>Классическую клетчатую плиссированную юбку принято называть шотландкой. Такой вариант подойдет, в хорошем смысле слова, «легкомысленным» девушкам, для которых создание своего наряда – это постоянная игра в образы. Отличную модель именно с таким «подтекстом», я нашла в осенне-зимнем каталоге "Снежной Королевы" – <a href="http://snowqueen.ru/collection/wclothes/4747/">юбка в стиле девочки-школьницы</a>. Сочетать такую, стоит либо с классической белой блузой, либо с джемпером кремового или темного (коричневого, черного, серого) оттенков. <a href="http://snowqueen.ru/collection/wclothes/4473/">Платье</a> с оригинально драпированной зоной юбки станет прекрасной альтернативой «школьной» юбке.
<p>Более серьезным барышням, предпочитающим подчеркивать свою фигуру моделями прямого кроя, подойдет <a href="http://snowqueen.ru/collection/wclothes/4769/">юбка-карандаш</a> в актуальную клетку сдержанной длины до колена. Или же <a href="http://snowqueen.ru/collection/wclothes/4470/">платье</a> насыщенного красного оттенка – отличный вариант для офиса!
<p>Для свидания я рекомендую приглядеться к темно-синему платью-футляру с интригующим V-образным вырезом и изящным <a href="http://snowqueen.ru/collection/wdress/4557/">поясом</a>, выгодно подчеркивающим талию. 
<p>Во время всей этой «игры в клетку» стоит помнить одно важное правило – не переборщить! Делая выбор в пользу платья или юбки в клетку, подбирайте однотонные аксессуары, сумку и обувь. Клетка не терпит конкуренции – она всегда должна оставаться в центре внимания!
<p>Удачи вам в осваивании новых осенних трендов! :)


