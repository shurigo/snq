            <p>За что я люблю лето? Во-первых, за то, что можно ходить без чулок (и призываю всех женщин делать тоже самое!). Во-вторых, за то, что можно без риска поскользнуться, и переломать себе все на свете,  носить каблуки. Ну и в третьих, за то, что носить самые разнообразные принты более, чем оправдано.  Этника, флора и фауна, психоделика, фото-принты и геометрия – выбирайте то, что вам по душе. Только не бойтесь! Многие ограничиваются цветочным принтом или полоской. Но это так просто! Комбинируйте! Зимой вы позволить себе этого не сможете. Все, что дизайнеры предложат вам в следующем сезоне – клетка и полоска. Стандартные зимние рисунки, что сказать.</p>
            <p>Я долгое время изучала африканское декоративно-прекладное искусство, в том числе текстиль и одежду. Так вот, хочу вам сказать, что у африканских народов форма одежды не так важна, как декор и орнамент. И они мастера в сочетании самых невероятных цветов и рисунков. Нам, конечно, далеко до идеала, но всегда есть к чему стремиться. Могу дать несколько советов: геометрические рисунки хорошо сочетаются между собой. Сочетать разные растительные орнаменты – большое искусство и требует определенных навыков. Поэтому, если вы не уверены в себе, лучше ограничиться только одной вещью «в цветочек». Помните, что вещи разных орнаментов должны быть в одной цветовой гамме, иначе вас ждет полный провал.</p>
